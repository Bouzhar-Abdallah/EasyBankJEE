package Objects;

public class Simulation {
    private float mensualite;
    private float capitalEmprunte;
    private float taux;
    private int nombreMensualite;

    public float getMensualite() {
        return mensualite;
    }

    public void setMensualite(float mensualite) {
        this.mensualite = mensualite;
    }

    public float getCapitalEmprunte() {
        return capitalEmprunte;
    }

    public void setCapitalEmprunte(float capitalEmprunte) {
        this.capitalEmprunte = capitalEmprunte;
    }

    public float getTaux() {
        return taux;
    }

    public void setTaux(float taux) {
        this.taux = taux;
    }

    public int getNombreMensualite() {
        return nombreMensualite;
    }

    public void setNombreMensualite(int nombreMensualite) {
        this.nombreMensualite = nombreMensualite;
    }
}
